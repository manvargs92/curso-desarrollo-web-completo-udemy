// Orden de las operaciones

let resultado;

resultado = 20 + 30 * 2;
resultado = (20 + 30) * 2;

resultado = (100 + 200 + 300) * .2; // un descuento

resultado = (100 + 200 + 300) * 1.16; // un impuesto

resultado = (100 + 200) * 1.05; // una comisión

